    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if (env('DEMO_MODE') == 'On') {
            flash(translate('This action is disabled in demo mode'))->error();
            return back();
        }
        try{	
			DB::beginTransaction();
			$model = new Cost();
			
			
			$model->fill($_POST['Cost']);
            if(!isset($_POST['Cost']['to_country_id']))
            {
                $model->to_country_id = $_POST['Cost']['from_country_id'];
            }
			if (!$model->save()){
				throw new \Exception();
			}
			
			DB::commit();
            flash(translate("Cost added successfully"))->success();
            return back();
		}catch(\Exception $e){
			DB::rollback();
			print_r($e->getMessage());
			exit;
			
			flash(translate("Error"))->error();
            return back();
		}
    }

